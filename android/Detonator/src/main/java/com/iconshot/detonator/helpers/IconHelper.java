    public static String getFont(String name) {
        for (Map.Entry<String, String> entry : fonts.entrySet()) {
            if (name.startsWith(entry.getKey())) {
                return entry.getValue();
            }
        }

        return null;
    }

    public static Character getIcon(String key) {
        return icons.get(key);
    }

    public static Typeface getTypeface(Context context, String font) {
        Typeface typeface = typefaces.get(font);

        if (typeface == null) {
            typeface = Typeface.createFromAsset(context.getAssets(), "fonts/" + font);

            typefaces.put(font, typeface);
        }

        return typeface;
    }
