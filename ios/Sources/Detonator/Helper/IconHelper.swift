    public static func addFont(key: String, name: String) {
        fonts[key] = name
    }

    public static func addIcon(key: String, iconChar: String) {
        icons[key] = iconChar
    }

    static func getFont(name: String) -> String? {
        for (key, value) in fonts {
            if name.starts(with: key) {
                return value
            }
        }
        
        return nil
    }

    static func getIcon(key: String) -> String? {
        return icons[key]
    }
